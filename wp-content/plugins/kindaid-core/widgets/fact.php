    protected function render(): void {
        $settings = $this->get_settings_for_display();

        if(empty($settings['list'])){
            return;
        }

        $fact_count = count($settings['list']);
        $fact_count -= 1;

        ?>
        <div class="tp-fact-area tp-bg-mulberry pt-40 pb-35">
            <div class="container container-1424">
                <div class="row">
                    <?php foreach ($settings['list'] as $key => $item): ?>
                        <?php
                        $fact_border_class = $fact_count == $key ? '' : 'tp-fact-item-border';
                            ?>
                        <div class="col-xl-3 col-lg-3 col-md-6 col-sm-6">
                            <div class="tp-fact-item <?php echo esc_attr($fact_border_class); ?> text-center pt-20 pb-25">
                                <h2 class="tp-fact-title mb-5"><span class="purecounter" data-purecounter-duration="2"
                                        data-purecounter-end="64"><?php echo esc_html($item['number']); ?></span><?php echo esc_html($item['sign']); ?></h2>
                                <p class="tp-fact-dec mb-0"><?php echo esc_html($item['content']); ?></p>
                            </div>
                        </div>
                    <?php endforeach; ?>
                </div>
            </div>
        </div>
        <?php
    }
